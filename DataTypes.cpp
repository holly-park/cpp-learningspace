#include<iostream> //contain information that is necessary

using namespace std; //what use to compiler

int main(){
    cout <<"Size of char : " <<sizeof(char)<<"\t";
    cout <<"Size of int : " <<sizeof(int)<<"\t";
    cout <<"Size of short int : " <<sizeof(short int)<<"\t";
    cout <<"Size of long int : " <<sizeof(long int)<<endl;
    cout <<"Size of float : " <<sizeof(float)<<"\t";
    cout <<"Size of double : " <<sizeof(double)<<"\t";
    cout <<"Size of long double : " <<sizeof(long double)<<"\t";
    cout <<"Size of wchar_t : " <<sizeof(wchar_t)<<endl;
    return 0;
}
